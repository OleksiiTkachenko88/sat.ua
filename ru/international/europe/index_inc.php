<p class="dline">
 <span style="font-size: 13pt;">Когда есть &nbsp;&nbsp;необходимость доставить груз в/из Европы, можно экспериментировать. А можно поручить задание SAT. Наши услуги по международной перевозке – оптимальное решение для бизнеса. Доставим от одной единицы до оптовых партий товаров. </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b>ЧТО МЫ ДОСТАВЛЯЕМ</b> </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Компания SAT перевозит различные виды грузов по территории Европы и стран СНГ. Вес груза – от 0,01 кг до 20&nbsp;000 кг (Parcel/LTL/FTL). </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b>НАШИ ПРЕИМУЩЕСТВА</b> </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Оптимальный тариф – наличие представительства в Европе позволяет предлагать клиенту по-настоящему выгодные решения </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Универсальность – перевозим грузы весом от 0,01 кг до 20&nbsp;000 кг. </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Сервис – предоставляем комплекс международных логистических услуг </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Сеть представительств в Украине – решение для быстрой доставки товаров покупателям</span><span style="font-size: 13pt;">&nbsp;</span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b><span style="font-size: 15pt;">Перевозки автомобильным транспортом:</span></b></span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b>ЭКСПОРТ</b> </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	По запросу клиента SAT отправит груз из любого нашего представительства (отделения), а также выедет за грузом по указанному им адресу. Компания доставит груз до указанного адреса в Европе/СНГ или выдаст его в своём представительстве в Польше (г. Перемышль) </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b>ИМПОРТ</b> </span>
