 <span style="font-size: 13pt;"> </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Мы &nbsp;выедем за грузом по указанному адресу в Европе/СНГ или отправим его в Украину из нашего представительства в Польше (г. Перемышль). В Украине доставим груз к любому доступному представительству SAT или по адресу, указанному клиентом.</span>
