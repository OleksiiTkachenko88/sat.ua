 Предоставление сверхпочтовых услуг – от адресной доставки до управления заказами и отслеживания движения грузов. В функции поставщика услуг входит организация и управление перевозками, учет и управление запасами, подготовка импортно-экспортной и фрахтовой документации, складское хранение, обработка груза, доставка конечному потребителю. <br>
