<p style="text-align: center;">
 <b>2. Опис процесу платежу</b>
</p>
<p>
	 2.1. Оплата&nbsp; послуг&nbsp;здійснюється&nbsp;на&nbsp;сайті Перевізника. Сайт Перевізника переадресовує Клієнта на сайт інформаційно-телекомунікаційної системи UAPAY.
</p>
<p>
	 2.2. На&nbsp;захищеній&nbsp; сторінці&nbsp;UAPAY Клієнт зазначає інформацію про свою платіжну карту. Будь ласка завчасно приготуйте свою платіжну карту для здійснення платежу.
</p>
<p>
	 2.3. Для&nbsp;здійснення&nbsp; оплати&nbsp;послуг&nbsp;з&nbsp;перевезення&nbsp; на&nbsp;сторінці&nbsp;UAPAY Клієнту необхідно знати номер ТТН та дату її формування.
</p>
<p>
	 2.4. Перейшовши за посиланням на сайті Перевізника, Клієнт побачить кнопку «Оплатити», при натисканні на яку запит буде переадресовуватись на захищену сторінку UAPAY. На сторінці платіжної системи Клієнту необхідно ввести дані своєї платіжної карти: номер, термін дії, CVV-код. Далі дійте за інструкціями платіжної системи.
</p>
<p>
	 2.5. Оплата здійснюється з використанням платіжних карток Visa/MasterCard.
</p>
<p>
	 2.6. Послуги вважаються сплаченими належним чином з моменту зарахування коштів на поточний рахунок Перевізника, що підтверджується відповідною квитанцією, яка надсилається на електронну пошту Клієнта після здійснення оплати.
</p>
<p>
	 2.7. Валюта здійснення онлайн оплати послуг з перевезення вантажів – українська гривня.
