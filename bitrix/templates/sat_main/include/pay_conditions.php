</p>
<p>
	 1.2. Перевізник&nbsp; зобов’язується&nbsp;доставити&nbsp;надані Клієнтом вантажі до пункту призначення в установлений строк та видати їх уповноваженій на одержання вантажу особі – вантажоодержувачу, а Клієнт зобов’язується вчасно сплатити за послуги Перевізника встановлену плату.
</p>
<p>
	 1.3. Погоджуючись&nbsp; з&nbsp;Умовами&nbsp;онлайн&nbsp;оплати&nbsp;послуг&nbsp; з&nbsp;перевезення&nbsp;вантажів, Клієнт&nbsp; тим&nbsp;самим&nbsp;підтверджує, що&nbsp;ознайомився, зокрема, з тарифами Перевізника, умовами перевезення вантажів ТОВ «ТК «САТ», правилами упакування вантажів, що розміщені на офіційному сайті Перевізника&nbsp;<a href="https://www.sat.ua/" target="_blank">https://www.sat.ua/</a>, та погоджується з їх положеннями.
</p>
<p>
	 1.4. В&nbsp;разі&nbsp; незгоди&nbsp;із&nbsp;зазначеним&nbsp;в&nbsp;п. 1.3. цих&nbsp;Умов&nbsp;Клієнт&nbsp;зобов’язаний&nbsp; негайно&nbsp;припинити&nbsp;використання&nbsp; сервісу&nbsp;і&nbsp;покинути&nbsp;сайт&nbsp;<a href="https://www.sat.ua/" target="_blank">https://www.sat.ua/</a>.
