<p style="text-align: center;">
 <b>
	4. Перевезення </b>
</p>
<p>
	 4.1. Перевезення вантажу до місця призначення виконується в термін не більш ніж 3 (три) дні з дати оформлення ТТН (ННВ), за виключенням неділі та святкових днів, за визначеними Перевізником маршрутами.
</p>
<p>
	 4.2. Строк&nbsp; перевезення, вказаний&nbsp;в 4.1. може&nbsp; бути&nbsp;збільшений, якщо на перевезення окремого виду вантажу встановлено окремі тарифи та строк перевезення, вказані у прайсі Перевізника, розміщенному на сайті&nbsp;<a href="https://www.sat.ua/" target="_blank">https://www.sat.ua/</a>.
