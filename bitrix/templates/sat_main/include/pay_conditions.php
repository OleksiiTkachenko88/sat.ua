</p>
<p>
	 3.1. Оплата послуг здійснюється Клієнтом (володільцем платіжної карти) з використанням інформаційно-телекомунікаційна система&nbsp;UAPAY&nbsp;на користь та на поточний рахунок Перевізника – ТОВ «ТК «САТ».
</p>
<p>
	 3.2. На сайті ТОВ «ТК «САТ» не зберігаються дані платіжних карт Клієнтів та інформація щодо них не передається 3-тім особам. Інформація по платіжним картам вноситься Клієнтом на захищеній сторінці національного платіжного сервісу <a href="http://uapay.ua">www.uapay.ua</a>, що відповідає стандартам інформаційної безпеки PCI DSS 3.2 і гарантує повну безпеку Ваших даних.
</p>
<p>
	 3.2. При відмові вантажоодержувача від отримання вантажу, кошти оплачені за допомогою платіжної картки в автоматичному режимі не повертаються.
</p>
