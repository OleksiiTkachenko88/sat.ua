<p>
 <b><span style="font-size: 15pt;">Міжнародні авіаперевезення</span></b>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">&nbsp;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">Компанія SАТ пропонує послуги по організації доставки документів і вантажів авіатранспортом в більш ніж в 220 країн світу (експорт / імпорт)</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">&nbsp;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <b><span style="font-size: 15pt;">Наші переваги:</span></b>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- можливість відправити лист, документи, вантаж, як з адреси, так і з будь-якого відділення SАТ в Україні;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- адресна доставка до дверей одержувача;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- надійність;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- швидка доставка від 5 робочих днів</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">&nbsp;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <b><span style="font-size: 15pt;">Формат відправлень:</span></b>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- Листи;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- Документи;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- Вантажі до 30 кг;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">- Максимальні габарити вантажу, що приймається до перевезення: 120см * 50см * 50см</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">&nbsp;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <b><span style="font-size: 15pt;">Додаткові послуги:</span></b>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">Митно - брокерські послуги;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">Упаковка</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <b><span style="font-size: 15pt;">Список вантажів,&nbsp; що заборонені до авіаперевезення:&nbsp;</span></b>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">зброя - вогнепальна, газова, пневматична, холодна;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">вибухові речовини і боєприпаси - порох, патрони, піротехніка, динаміт, детонатори;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">гази - стислі і зріджені, як великі балони, так і газові балончики, аерозолі;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">легкозаймисті рідини та тверді речовини - пальне, ацетон, ефіри, метиловий ефір, певні види калію, натрію, кальцію, фосфору, нитроцеллюлоза;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">будівельні хімічні речовини;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">отруйні речовини - всі отруйні &nbsp;речовини, незалежно від їх агрегатного стану та упаковки;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">кислоти та корозійні речовини - сірчана, соляна, сірчана-воднева кислоти;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">рідкісні, дорогоцінні метали, каміння, вироби з них, гроші та їх еквіваленти;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">ракетні складові, пристрої зв'язку для військової сфери, документація, розробки та креслення на ці пристрої;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">наркотики і психотропи, етиловий спирт;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">тварини, рослини, вакцини, біологічні зразки, людські органи;</span>
</p>
 <span style="font-size: 15pt;"> </span>
<p>
 <span style="font-size: 15pt;">радіоактивні речовини та пристрої, де їх застосовують.</span>
