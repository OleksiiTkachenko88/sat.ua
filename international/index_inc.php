	<span style="font-size: 13pt;">Коли нашим клієнтам необхідно зробити міжнародну доставку, SAT завжди приходить з готовим рішенням. </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	До ваших послуг - міжнародне перевезення авіа- і автотранспортом (експорт / імпорт) у форматі Parcel / LTL / FTL (як по території України, так і за кордоном). Перевезти збірний бізнес-вантаж будь-якого формату? У SAT вже є надійні і одночасно економічні рішення. </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Наші клієнти можуть відправити або отримати міжнародне відправлення / вантаж на будь-якому представництві SAT, а також скористатися адресним обслуговуванням як в Україні, так і за її межами. При цьому, вантаж заберуть із зазначеного місця і доставлять до кінцевого пункту призначення в найоптимальніші терміни. </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Користуючись послугами SAT, ви отримуєте «єдине вікно» на всьому шляху вантажу. У той же час, мережа представництв в Україні забезпечить швидку доставку товарів покупцям. </span>
