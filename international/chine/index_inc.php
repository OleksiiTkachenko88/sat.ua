 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"> <b>Авіаперевезення:</b> </span>
</p>
 <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;">
	Доставка авіатранспортом - оптимальні рішення для невеликих і дорогих товарів. Товарів, для яких термін доставки є пріоритетом (електроніка, комплектуючі та запчастини і т.д.). Це найшвидший спосіб перевезення з Китаю.</span>
</p>
<p class="dline">
 <span style="font-size: 13pt;">- Транзитний час - 7-14 робочих днів</span>
</p>
<p class="dline">
 <span style="font-size: 13pt;">- Гнучкі тарифи</span>
</p>
<p class="dline">
	<span style="font-size: 13pt;">- </span><span style="font-size: 13pt;">Доставка до представництва SAT або до дверей</span>
</p>
<p>
</p>
 <span style="font-size: 13pt;"> </span> <span style="font-size: 13pt;"> </span> <span style="font-size: 13pt;"> </span>
<p class="dline">
 <span style="font-size: 13pt;"><b>Морські перевезення:</b></span>
